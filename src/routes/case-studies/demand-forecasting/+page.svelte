<h2 class="text-2xl mb-6">The Challenge</h2>
<p class="mb-6">The money lies in optimizing inventory. Keeping excess stock ties up capital, while stockouts lead to missed sales opportunities. The client was heavily reliant on guesswork and manual forecasts, which often resulted in:</p>
<ul class="mb-6">
    <li><span class="font-semibold">Overstocking:</span> Higher storage costs and markdowns to clear unsold inventory.</li>
    <li><span class="font-semibold">Stockouts:</span> Missed sales and dissatisfied customers, especially for trending items.</li>
    <li><span class="font-semibold">Inefficient Supply Chain:</span> Difficulty in predicting the demand for new and seasonal products, causing disruptions with suppliers.</li>
    <li><span class="font-semibold">High Wait Time:</span> Long lead times from suppliers made it difficult for the client to replenish stock quickly, resulting in delayed product availability and potential lost sales opportunities.</li>
</ul>

